package BaiBuoi6_7.Bai2;

public class Khoi {
    private String mon1;
    private String mon2;
    private String mon3;

    public Khoi() {

    }
    public Khoi(String mon1, String mon2, String mon3) {
        this.mon1 = mon1;
        this.mon2 = mon2;
        this.mon3 = mon3;
    }

    public String getMon1() {
        return mon1;
    }
    public void setMon1(String mon1) {
        this.mon1 = mon1;
    }
    public String getMon2() {
        return mon2;
    }
    public void setMon2(String mon2) {
        this.mon2 = mon2;
    }
    public String getMon3() {
        return mon3;
    }
    public void setMon3(String mon3) {
        this.mon3 = mon3;
    }
}
